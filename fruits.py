# List indexing

my_list = ['papaya', 'apple', 'orange', 'banana', 'grapes']

print(my_list[0])

print(my_list[2])

print(my_list[4])
my_list.append('litchi')

print(my_list)

my_list.extend(['chickoo','custard apple'])

print(my_list)
del my_list[2]
print(my_list)

# Nested List
n_list = ["welcome", [2, 0, 1, 9]]

# Nested indexing
print(n_list[0][1])

print(n_list[1][3])

